       $p1 = substr($Players[$iPlayerNum]["tour".$iTourNum][$ind], 0, 1);
       $p2 = substr($Players[$iPlayerNum]["tour".$iTourNum][$ind], 2, 1);

       if ((($s1 != "?")&&($s2 != "?"))&&(($p1 != "?")&&($p2 != "?")))
        {

           if ((($s1 > $s2) && ($p1 < $p2)) || (($s1 < $s2) && ($p1 > $p2)))
           {
               $Points -= 1;
           }
           else
           {
